class Net(nn.Module):
    def __init__(self, vgg, decoder, tv_weight=1e-8, pretrain=False):
        super(Net, self).__init__()
        enc_layers = list(vgg.children())
        self.enc_1 = nn.Sequential(*enc_layers[:4])  # input -> relu1_1
        self.enc_2 = nn.Sequential(*enc_layers[4:11])  # relu1_1 -> relu2_1
        self.enc_3 = nn.Sequential(*enc_layers[11:18])  # relu2_1 -> relu3_1
        self.enc_4 = nn.Sequential(*enc_layers[18:31])  # relu3_1 -> relu4_1
        self.decoder = decoder
        self.vgg = vgg
        self.tv_weight = tv_weight
        self.mse_loss = nn.MSELoss()
        self.cls_loss = nn.CrossEntropyLoss()

        # fix the encoder
        for name in ["enc_1", "enc_2", "enc_3", "enc_4"]:
            for param in getattr(self, name).parameters():
                param.requires_grad = False

    # extract relu4_1 from input image
    def encode(self, input):
        for i in range(4):
            input = getattr(self, "enc_{:d}".format(i + 1))(input)
        return input

    def decode(self, feature):
        output = self.decoder(feature)
        return output

    def pretrain(self, input):
        feat = self.encode(input)
        output = self.decoder(feat)
        diff_i = torch.sum(torch.abs(output[:, :, :, 1:] - output[:, :, :, :-1]))
        diff_j = torch.sum(torch.abs(output[:, :, 1:, :] - output[:, :, :-1, :]))
        loss_tv = diff_i + diff_j
        loss = self.mse_loss(output, input)
        return loss + self.tv_weight * loss_tv

    def _forward_impl(self, x, stage):
        if stage == "encode":
            return self.encode(x)
        elif stage == "decode":
            return self.decode(x)
        else:
            print("no such stage: {:s}".format(stage))

    def forward(self, x, stage):
        return self._forward_impl(x, stage)
